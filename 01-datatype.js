// primitive Datatype: Number,String and Boolean

// ---------------- Number datatype------------
var age = 22;
console.log("Age is : ",age);

var ageTypeof = typeof age;
console.log("Datatype of age is :", typeof age);

// ------------------String datatype-------------

var city = 'Pune';
console.log("city is :", city);

var cityTypeof = typeof city;
console.log("Datatype of city is :", typeof city);

var marks = "93";
console.log("mark is :", marks);

var MarksTypeof = typeof marks;
console.log("Datatype of mark is :", typeof marks);

// ----------------------Boolean datatype--------------

var isMarried = true;
console.log("isMarried:", isMarried);

var isMarriedTypeof = typeof isMarried;
console.log("Datatype of isMarried is :", typeof isMarried);

var isFailed = false;
console.log("isFailed:", isFailed);

var isFailedTypeof = typeof isFailed;
console.log("Datatype of isFailed is :", typeof isFailed);


// --------------------Special Datatype : Undefined-----------


var country;
console.log('Country value is :', country);

var countryTypeof = typeof country;
console.log("Datatype of country is :", typeof country);


// ------------------------special Datatype : Null-------------

var village = null;
console.log("Value of village is :", village);

var villagetypeof = typeof village;
console.log("Datatype of village is :", villagetypeof);
